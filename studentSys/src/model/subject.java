package model;

public class subject {
	private String sub;
	private int score=0;
	
	public subject(String sub) {
		this.sub=sub;
	}
	public String getSub() {
		return sub;
	}
	public void setSub(String sub) {
		this.sub = sub;
	}
	public int getScore() {
		return score;
	}
	public void setScore(int score) {
		this.score = score;
	}

	
	
}
